package JavaClass;

public class StringMethod {

	public static void main(String[] args) {
		
		String name = "vasanth";
		
		System.out.println(name.charAt(2));
		
		System.out.println(name.equals("Vasanth"));
		
		System.out.println(name.equalsIgnoreCase("Vasanth"));
		
		System.out.println(name.length());
		
		System.out.println(name.toUpperCase());
		
		System.out.println(name.toLowerCase());
		
		System.out.println(name.contains("vasa"));
		
		System.out.println(name.isEmpty());
		
		System.out.println(name.lastIndexOf(2));
		
		System.out.println(name.concat("kumar"));
		
		System.out.println(name.replace("vasanth", "VASANTH"));
		
		System.out.println(name.isBlank());
		
		
		
		
		
		
		
	}

}
